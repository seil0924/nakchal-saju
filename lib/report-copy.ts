// 鏡 — 해외 거장(작고) 닮은 사주 (무료 바이럴 훅)
function twinHtml(tm:TyMatch, me:number, myDist:number[], myGan?:string){
  const lv = tm.level==='twin' ? '닮은 사주' : tm.level==='near' ? '가까운 사주' : '결이 비슷한 사주';
  const t=tm.tycoon;
  const dl=(d:number[])=>d.map((n,i)=>`${EL[i]}${n}`).join('·');
  const chips = tm.matched.length
    ? `<div class="twinchips">${tm.matched.map(m=>`<span class="twc">${m}</span>`).join('')}</div>`
    : '';
  const sim = tm.count ? Math.round(52+tm.count/6*46) : 68;
  const myGanChar = myGan || EL[me];
  const tyGanChar = (tm.pills||'')[0] || EL[tm.el];
  const sealcmp = `<div class="sealcmp"><div class="scin">`+
    `<div class="scside"><div class="scwrap">${sealSvg(myDist,myGanChar,132)}</div><div class="scnm">대표님</div><div class="scr">${EL[me]} 기운</div></div>`+
    `<div class="scmid"><div class="scpct">${sim}<em>%</em></div><div class="scpl">닮음</div></div>`+
    `<div class="scside"><div class="scwrap">${sealSvg(tm.tyDist,tyGanChar,132)}</div><div class="scnm">${t.name}</div><div class="scr">${EL[tm.el]} 기운</div></div>`+
    `</div><div class="sccap">얼굴이 아니라 <b>명식(命式)의 문양</b>입니다 — 두 인장의 오행 비율이 이만큼 겹칩니다</div></div>`;
  return `<div class="twinlead">대표님의 명(命)을 세계 거장 100인과 견줬습니다.<br>`+
    `가장 <b>${lv}</b>는 이 사람 — <b>${t.name}</b>입니다.</div>`+
    sealcmp+
    `<div class="twincard">`+
      `<div class="tface" style="background:${EL_HEX[tm.el]}">${tm.pills}</div>`+
      `<div class="tinfo"><div class="tnm">${t.name}</div>`+
      `<div class="ten">${t.en}</div><div class="tco">${t.co}</div></div>`+
    `</div>`+
    (tm.profile?`<div class="typrof"><div class="tprep">“${tm.profile.rep}”</div>`+
      `<div class="tpmeta"><span class="tpm"><i>분야</i>${tm.profile.field}</span><span class="tpm"><i>활동</i>${tm.profile.life}</span></div>`+
      `<div class="tpscale"><span class="tpsl">전성기 · 이룬 것</span><p>${tm.profile.scale}</p></div>`+
      `<div class="tparc"><span class="tpsl">연혁</span><p>${tm.profile.arc}</p></div></div>`:'')+
    `<div class="myeong"><div class="myl">命 · ${TYPE_NAME[me]} — ${TYPE_DESC[me]} 명</div>`+
      `<p>${TYPE_MYEONG[me]}</p>`+
      `<p class="mys">이 거장을 일으킨 것은 재능이나 운이 아니라 <b>바로 이 명(命)</b>이었습니다. <b>${t.name}</b> — ${tm.story}</p>`+
      `<p class="mys2">맨손에서 시작해 <b>무(無)에서 유(有)를 이룬 사람</b>. 그 뿌리의 기운이, 대표님 여덟 글자와 같은 것입니다.</p></div>`+
    `<p class="twbasis">대표님 명식 <b>${dl(myDist)}</b> · ${t.name} 삼주 <b>${dl(tm.tyDist)}</b> — `+
    (tm.count?`두 명식에서 <b>${tm.matched.join('</b>·<b>')}</b>, 여섯 부호 중 ${tm.count}가지가 겹칩니다.`:`구조의 결이 닿습니다.`)+`</p>`+
    chips+
    `<div class="mecard"><div class="mel">그리고 — 대표님이 그 명(命)입니다</div>`+
      `<p class="mep">${t.name}을 일으킨 그 <b>${EL[me]}의 기운</b>이, 대표님 여덟 글자에 같은 뼈대로 앉아 있습니다.</p>`+
      `<p class="mep">${TYPE_USER[me]}</p>`+
      `<div class="merule"></div>`+
      `<p class="mep">${TYPE_BIZ[me]}</p></div>`+
    `<p style="margin-top:12px">다만 ${TYPE_RISK[me]}<br>그러니 ${TYPE_WAY[me]}</p>`+
    `<p class="twinnote">※ 인물 명식은 널리 공개된 출생일 기준이며 생시(生時)는 미상이라 삼주(三柱)로만 계산했습니다. 명식의 구조를 견준 것으로, 재미로 보는 유형 비교일 뿐 그분들의 삶이나 대표님의 운을 단정하는 것이 아닙니다.</p>`;
}

// 투찰 택일: 이번 달 길일 캘린더 (대표 일간을 살리는 일진 = 상단/생 날)
function choilHtml(c:Chart, y:number, m:number){
  const first=new Date(Date.UTC(y,m-1,1)).getUTCDay();
  const days=new Date(Date.UTC(y,m,0)).getUTCDate();
  const gilDays:number[]=[];
  let cells='';
  for(let i=0;i<first;i++) cells+='<div class="cd mute"></div>';
  for(let d=1;d<=days;d++){
    const rel=relation(c.dayMasterEl, todayPillar(y,m,d).el);
    const gil=(rel==='in'||rel==='bi'); if(gil) gilDays.push(d);
    cells+=`<div class="cd${gil?' gil':''}">${d}</div>`;
  }
  const dh=['일','월','화','수','목','금','토'].map(x=>`<div class="cdh">${x}</div>`).join('');
  return `<div class="choil">${dh}${cells}</div>`+
    `<p>이번 달 <b>${m}월</b>, 대표님 기운이 위로 뻗는 <b>투찰 길일</b>은 <b>${gilDays.length?gilDays.join('·')+'일':'해당 없음'}</b>입니다.</p>`+
    `<p>오늘의 사정률과 같은 원리로, 대표님 일간(${GAN[c.dGan]})을 살리는 일진이 드는 날 — 큰 건 투찰·계약·미팅을 이 날에 맞추면 유리합니다.</p>`+
    `<p>반대로 표시 없는 날은 기운이 눌리니, 무리한 저가 투찰은 피하고 관망하십시오.</p>`;
}

